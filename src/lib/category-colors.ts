// ライトモード・ダークモード両方で視認性が確保された色を選定
export const CATEGORY_COLORS: Record<string, string> = {
  '食費': '#EF5350',       // コーラルレッド — 食欲・暖かさ (0°)
  '光熱費': '#FFA726',     // アンバーオレンジ — エネルギー・炎 (33°)
  '日用品': '#D4E157',     // イエローグリーン — 日常・ナチュラル (64°)
  '医療': '#66BB6A',       // グリーン — 健康・医療 (123°)
  '通信費': '#26C6DA',     // シアン — デジタル・テクノロジー (184°)
  '交通費': '#42A5F5',     // ブルー — 移動・交通 (213°)
  'ショッピング': '#7E57C2', // ディープパープル — 買い物・ラグジュアリー (262°)
  '娯楽': '#E040FB',       // エレクトリックマゼンタ — 遊び・エンタメ (280°)
  'おしゃれ': '#F06292',   // ローズピンク — ファッション・美容 (340°)
  'その他': '#90A4AE',     // ブルーグレー — ニュートラル
};

// カテゴリ名から色を取得（未定義カテゴリはグレー）
export function getCategoryColor(category: string): string {
  return CATEGORY_COLORS[category] ?? CATEGORY_COLORS['その他'] ?? '#90A4AE';
}
